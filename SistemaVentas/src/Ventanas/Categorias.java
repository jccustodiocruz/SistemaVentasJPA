    public void llenarTabla() {
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        List<Category> lc = cr.findCategoryEntities();
        Object o[] = null;

        for (int i = 0; i < lc.size(); i++) {
            modelo.addRow(o);
            modelo.setValueAt(lc.get(i).getId(), i, 0);
            modelo.setValueAt(lc.get(i).getName(), i, 1);
            modelo.setValueAt(lc.get(i).getDescription(), i, 2);           
        }
        
        for (int i = 0; i < tCategorias.getColumnCount(); i++) {
            tCategorias.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
    }

    DefaultTableModel modelo;
    DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();

    private void modeloTabla() {
        try {
            modelo = (new DefaultTableModel(
                    null, new String[]{
                        "ID", "Name",
                        "Description"}) {
                        Class[] types = new Class[]{
                            java.lang.Integer.class,
                            java.lang.String.class,
                            java.lang.String.class
                        };
                        boolean[] canEdit = new boolean[]{
                            false, false, false
                        };

                        @Override
                        public Class getColumnClass(int columnIndex) {
                            return types[columnIndex];
                        }

                        @Override
                        public boolean isCellEditable(int rowIndex, int colIndex) {
                            return canEdit[colIndex];
                        }
                    });
            tCategorias.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString() + "error2");
        }

        tCategorias.getColumnModel().getColumn(0).setPreferredWidth(80);
        tCategorias.getColumnModel().getColumn(0).setMaxWidth(80);
        tCategorias.getColumnModel().getColumn(1).setPreferredWidth(150);
        tCategorias.getColumnModel().getColumn(1).setMaxWidth(150);
        
    }

